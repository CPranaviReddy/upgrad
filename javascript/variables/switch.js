let age =78;
switch(age){
case 30:
age="you are still younger";
console.log(age);
break;
case 50:
age="you are becoming older";
console.log(age);
break;
case 80:
age="you are very old";
console.log(age);
break;
case 82:
age="you are not a valid user";
console.log(age);
break;
default:
age=" If you are not eligible for all the above ages";
console.log(age);
break;
}

